// Helper function to generate task instances for a team member
const generateTaskInstances = (
    teamMemberId: string,
    projectId: string,
    completionPattern: ('COMPLETED' | 'IN_PROGRESS' | 'NOT_STARTED' | 'BLOCKED')[]
): TaskInstance[] => {
    const taskIds = [
        'task-001', 'task-002', 'task-003', 'task-010', 'task-011',
        'task-020', 'task-021', 'task-022', 'task-030', 'task-040'
    ];

    return taskIds.map((taskId, index) => ({
        id: `ti-${teamMemberId}-${taskId}`,
        taskId,
        teamMemberId,
        projectId,
        status: completionPattern[index % completionPattern.length],
        createdAt: '2025-01-01T00:00:00Z',
        updatedAt: '2025-01-14T00:00:00Z',
    }));
};

